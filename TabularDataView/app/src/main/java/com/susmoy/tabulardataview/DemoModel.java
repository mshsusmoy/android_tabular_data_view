package com.susmoy.tabulardataview;

public class DemoModel {
    String demo1;
    String demo2;
    String demo3;

    public DemoModel(String demo1, String demo2, String demo3) {
        this.demo1 = demo1;
        this.demo2 = demo2;
        this.demo3 = demo3;
    }

    public String getDemo1() {
        return demo1;
    }

    public void setDemo1(String demo1) {
        this.demo1 = demo1;
    }

    public String getDemo2() {
        return demo2;
    }

    public void setDemo2(String demo2) {
        this.demo2 = demo2;
    }

    public String getDemo3() {
        return demo3;
    }

    public void setDemo3(String demo3) {
        this.demo3 = demo3;
    }

}
